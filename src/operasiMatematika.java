public class operasiMatematika {
    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println(a += 10);
        System.out.println(a -= 10);
        System.out.println(a *= 10);
        System.out.println(a /= 10);
        System.out.println(a %= 10);

        int c =10;
        System.out.println(c++);
        System.out.println(c--);
        System.out.println(!true);

    }
}
